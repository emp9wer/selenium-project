package Self_PracticeKir.day06_IfElsePractice;

public class PozNegZero {
    public static void main(String[] args) {
        int number = -1;
        if(number==0){
            System.out.println("Zero");
        }
        if(number>=1){
            System.out.println("Positive");
        }
        if(number<0){
            System.out.println("Negative");
        }

    }
}
